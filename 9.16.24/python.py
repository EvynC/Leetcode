class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        n = len(nums)

        for i in range(0, n - 1, 1):
            
            for j in range(1, n, 1):
                if (nums[i] + nums[j] == target):
                    return [i, j]
        
